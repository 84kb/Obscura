const dictionaries: Record<AppLanguage, Record<string, string>> = { ja, en }

export function t(language: AppLanguage | undefined, key: string, params?: Params): string {
  const lang: AppLanguage = language === 'en' ? 'en' : 'ja'
  let value = dictionaries[lang][key] ?? dictionaries.ja[key] ?? key
  if (!params) return value
  for (const [k, v] of Object.entries(params)) {
    value = value.split(`{{${k}}}`).join(String(v))
  }
  return value
}
